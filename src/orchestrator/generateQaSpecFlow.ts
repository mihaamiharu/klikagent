import { QATask, TaskResult } from '../types';
import { log } from '../utils/logger';
import { runWithSelfCorrection } from '../services/selfCorrection';
import {
  getDefaultBranchSha,
  createBranch,
  openPR,
  commitFile,
} from '../services/github';
import { toSpecFileName, toBranchSlug } from '../utils/naming';

export async function generateQaSpecFlow(task: QATask): Promise<void> {
  const feature = task.feature ?? 'general';
  log('INFO', `[generateQaSpecFlow] Starting for task ${task.taskId} feature=${feature}`);

  // Create QA branch in the output repo
  const branch = toBranchSlug(task.taskId, task.title);
  const baseSha = await getDefaultBranchSha(task.outputRepo);
  await createBranch(task.outputRepo, branch, baseSha);
  log('INFO', `[generateQaSpecFlow] QA branch created: ${branch}`);

  // Derive spec path
  const specPath = `tests/web/${feature}/${toSpecFileName(task.title)}`;
  log('INFO', `[generateQaSpecFlow] Spec path: ${specPath}`);

  // Run self-correction loop (QA agent + tsc validation)
  const result = await runWithSelfCorrection(task, branch, specPath);
  const { specContent, poms, affectedPaths, tokenUsage } = result;

  // Commit spec + POMs to branch
  await commitFile(
    task.outputRepo, branch, specPath, specContent,
    `feat(spec): add #${task.taskId} spec [klikagent]`,
  );
  for (const { pomContent, pomPath } of poms) {
    await commitFile(
      task.outputRepo, branch, pomPath, pomContent,
      `feat(pom): add ${feature} POM for #${task.taskId} [klikagent]`,
    );
  }

  // Open draft PR in output repo
  const prUrl = await openPR(
    task.outputRepo,
    branch,
    `[QA] #${task.taskId} ${task.title}`,
    `Automated QA spec generated by KlikAgent.\n\nTask: ${task.taskId}\nAffected paths: \`${affectedPaths}\``,
  );

  log('INFO', `[generateQaSpecFlow] Done — PR opened: ${prUrl}`);

  // Notify trigger service via callbackUrl so it can comment on the originating ticket
  if (task.callbackUrl) {
    const taskResult: TaskResult = {
      taskId: task.taskId,
      passed: !result.warned,
      summary: result.warned
        ? `Spec generated with warnings — may need manual review. PR: ${prUrl}`
        : `Spec generated successfully. PR: ${prUrl}`,
      reportUrl: prUrl,
      metadata: {
        tokenUsage,
        warned: result.warned,
        warningMessage: result.warningMessage,
      },
    };
    await postCallback(task.callbackUrl, taskResult);
  }
}

async function postCallback(url: string, result: TaskResult): Promise<void> {
  try {
    const res = await fetch(url, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(result),
    });
    if (!res.ok) {
      log('WARN', `[generateQaSpecFlow] Callback to ${url} returned ${res.status}`);
    } else {
      log('INFO', `[generateQaSpecFlow] Callback posted to ${url}`);
    }
  } catch (err) {
    log('WARN', `[generateQaSpecFlow] Callback to ${url} failed: ${(err as Error).message}`);
  }
}
